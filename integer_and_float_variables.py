# Integer & Float variables (Numbers)

# print of a simple string variable
numeric_variable = 10
print("Integer variable value is : " +str(numeric_variable))

# Define simple variables for further manipulations
simple_variable_a = 5
simple_variable_b = 10

# Summery of 2 variables
summery_result = simple_variable_a + simple_variable_b
print("Summery result : " +str(summery_result))

# Mutiply 2 numbers
mutiply_result = simple_variable_a * simple_variable_b
print("Mutiply result : " +str(mutiply_result))

# Devide 2 numbers
devide_result = simple_variable_b / simple_variable_a
print("Devid result : " +str(devide_result))

# Power 2 numbers
power_result = simple_variable_a ** 2
print("Power result : " +str(power_result))

# Modulo action on 2 numbers
modulo_result = simple_variable_a % 2
print("Modulo result : " +str(modulo_result))

# Decimal point variable -> Float
decimal_variable_a = 2.5
decimal_variable_b = 0.25

summery_of_two_float = decimal_variable_a + decimal_variable_b
print("Summery of two float variables : " +str(summery_of_two_float))

